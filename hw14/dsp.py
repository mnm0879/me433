def apply_fir_filter(y, coefficients):
    n = len(y)
    m = len(coefficients)
    filtered_data = np.empty(n)

    for i in range(n):
        filtered_data[i] = sum(y[i - j] * coefficients[j] for j in range(m) if i - j >= 0)

    return filtered_data

